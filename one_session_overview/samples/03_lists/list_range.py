# Use range
# range(start, end) – start,…,end-1
#range(end) – 0,…,n-1
print("\nfor n in range(1,10):")
for n in range(1,10):	# 1-9
    print("n:", n)

print("\nfor n in range(10):")
for n in range(10):	# 0-9
    print("n:", n)

print("\nfor n in range(2,10,3):")
for n in range(2,10,3):	# 2 by 3 to before 10
    print("n:", n)
