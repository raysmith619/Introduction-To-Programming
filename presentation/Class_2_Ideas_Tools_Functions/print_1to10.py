# Demonstrate test while loop
print("\nTesting while loop")
start = 1
end = 10+1
n = start
while n < end:
    print("n:", n)
    n = n + 1
    
print("\nTesting for loop")
start = 1
end = 10+1
for n in range(start, end):
    print("n:", n)
    
